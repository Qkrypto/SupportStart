def kw_in(text: str, kw: str) -> bool:
    """Word-boundary keyword match ('mail' must not match inside 'email')."""
    return re.search(r"(?<!\w)" + re.escape(kw) + r"(?!\w)", text) is not None


def _score(entry: dict, text: str, category: str | None, device: str | None) -> float:
    score = 0.0
    for kw in entry["symptoms"]:
        if kw_in(text, kw):
            score += 3.0 + 1.5 * (len(kw.split()) - 1)  # specific multi-word phrases weigh more
    if category and category != "other" and category in entry["categories"]:
        score += 4.0
    if device and device.split()[0].lower() in entry["product"].lower():
        score += 1.0  # device is a weak hint; a specific symptom should always win
    return score


def match_entries(text: str, category: str | None = None, device: str | None = None,
                  top_n: int = 3) -> list[tuple[dict, float]]:
    """Rank KB entries against free text + intake hints. Returns [(entry, score)]."""
    text = (text or "").lower()
    ranked = sorted(
        ((e, _score(e, text, category, device)) for e in ENTRIES),
        key=lambda t: t[1], reverse=True,
    )
    return ranked[:top_n]


def select_entry(text: str, category: str | None = None, device: str | None = None
                 ) -> tuple[dict | None, list[dict], float]:
    """Pick the best entry, or return candidates when confidence is low.

    Returns (entry, candidates, confidence 0-1):
    - entry is not None  -> confident match, proceed
    - entry is None      -> ambiguous; ask the user to choose among candidates
    """
    ranked = match_entries(text, category, device, top_n=4)
    best, best_score = ranked[0]
    runner_score = ranked[1][1] if len(ranked) > 1 else 0.0

    if best_score == 0:
        # No symptom signal at all -> specific category default, else the general entry
        if category and category not in (None, "other"):
            for e in ENTRIES:
                if category in e["categories"]:
                    return e, [], 0.4
        return BY_ID["general_it"], [], 0.3
    # Ambiguous: close runner-up within the same intake category
    if runner_score > 0 and (best_score - runner_score) < 2.0 and best_score < 8.0:
        cands = [e for e, s in ranked if s > 0][:3]
        if len(cands) > 1:
            return None, cands, best_score / 10.0
    return best, [], min(best_score / 10.0, 1.0)


def kb_context_for_ai(conversation_text: str, category: str | None, device: str | None) -> str:
    """Format top KB matches as grounding context for the AI engine."""
    ranked = [t for t in match_entries(conversation_text, category, device, top_n=3) if t[1] > 0]
    if not ranked:
        ranked = [(BY_ID["general_it"], 0.0)]
    blocks = []
    for e, score in ranked:
        steps = "\n".join(
            f"  {i}. {s['title']['en']}. What: {s['what']['en']} | why: {s['why']['en']} "
            f"| expected: {s['expected']['en']} | difficulty: {s['difficulty']}"
            + (f" | visual: {s['visual']}" if s.get("visual") else "")
            for i, s in enumerate(e["steps"], 1)
        ) or "  (no self-service steps. Escalate after documenting)"
        qs = "\n".join(f"  - {q['q']['en']}" for q in e["questions"])
        r = e["routing"]
        blocks.append(
            f"### KB ENTRY: {e['id']} (match score {score:.1f})\n"
            f"Product: {e['product']}\nIssue: {e['issue']['en']}\n"
            f"Diagnostic questions:\n{qs}\nOrdered steps:\n{steps}\n"
            f"Escalation: {e['esc_reason']['en']}\n"
            f"Routing: {r['group']} | {r['category']} > {r['sub']} | priority {r['priority']} "
            f"| risk {r['risk']} | effort {r['effort']}"
        )
    return "\n\n".join(blocks)
